

export function useCorDeFundo (cor) {
 
        switch(cor){
            case "grass":
                return '#729F92';
            case "fire":
                return '#EAAB7D'
            case "water":
                return '#71C3FF'
            case "bug":
                return '#76A866'
            case "normal":
                return '#BF9762'
        }
}